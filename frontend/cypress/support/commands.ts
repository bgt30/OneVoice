declare global {
  namespace Cypress {
    interface Chainable {
      /**
       * 파일 업로드를 위한 커스텀 명령어
       * @example cy.attachFile('example.json')
       */
      attachFile(fileName: string): Chainable<Element>
    }
  }
}

// 기본 명령어 추가
Cypress.Commands.add('attachFile', { prevSubject: 'element' }, (subject, fileName) => {
  return cy.wrap(subject).attachFile(fileName);
});